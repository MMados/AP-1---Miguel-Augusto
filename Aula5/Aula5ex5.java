public class Aula5ex5 {
    public static void main(String [] args) {
        int contador=0;
        int num=5;
        while (contador <=17) {
            int resultado=num*contador;
            System.out.printf("%d X %d = %d\n",num,contador,resultado);
            contador++;
        }
    }
}
